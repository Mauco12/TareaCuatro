public class Perro {
    String nombreP;
    String tipoComidaP;
    int paseosP;
    String razaP;
    boolean vivoP;

    public Perro(String nombreP, String tipoComidaP, int paseosP,String razaP, boolean vivoP){
        this.nombreP = nombreP;
        this.tipoComidaP = tipoComidaP;
        this.paseosP = paseosP;
        this.razaP = razaP;
        this.vivoP = vivoP;


    }


}

